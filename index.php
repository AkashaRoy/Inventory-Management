Our tool is specially designed for systems that manage and store the requirement of items, and their subsequent distribution.
The entire process constitutes the creation of an Indent by any department that requires any item. This is followed by the creation of Purchase Orders against created indents. These purchase orders are provided to Vendors along with the due date. The vendors receive the purchase orders. When the vendor delivers the purchase order, it is received in the stock. This process includes updating the received item details like the number of goods arrived, damaged goods, etc. into the database. The corresponding purchase order is now received. These goods can now be issued to departments or students who require them.</font></div>


